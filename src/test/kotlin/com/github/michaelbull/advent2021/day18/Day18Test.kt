package com.github.michaelbull.advent2021.day18

import com.github.michaelbull.advent2021.day18.Day18.part1
import com.github.michaelbull.advent2021.day18.Day18.part2
import kotlin.test.Test
import kotlin.test.assertEquals

class Day18Test {

    @Test
    fun `example 1`() {
        val input = """
            [[[0,[5,8]],[[1,7],[9,6]]],[[4,[1,2]],[[1,4],2]]]
            [[[5,[2,8]],4],[5,[[9,9],0]]]
            [6,[[[6,2],[5,6]],[[7,6],[4,7]]]]
            [[[6,[0,7]],[0,9]],[4,[9,[9,0]]]]
            [[[7,[6,4]],[3,[1,3]]],[[[5,5],1],9]]
            [[6,[[7,3],[3,2]]],[[[3,8],[5,7]],4]]
            [[[[5,4],[7,7]],8],[[8,3],8]]
            [[9,3],[[9,9],[6,[4,9]]]]
            [[2,[[7,7],7]],[[5,8],[[9,3],[0,2]]]]
            [[[[5,2],5],[8,[3,7]]],[[5,[7,5]],[4,4]]]
        """

        assertEquals(4140, Day18.solve(::part1, input))
    }

    @Test
    fun `answer 1`() {
        assertEquals(3411, Day18.solve(::part1))
    }

    @Test
    fun `example 2`() {
        val input = """
            [[[0,[5,8]],[[1,7],[9,6]]],[[4,[1,2]],[[1,4],2]]]
            [[[5,[2,8]],4],[5,[[9,9],0]]]
